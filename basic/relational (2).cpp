#include<iostream>
using namespace std;
int main(){
	int a,b;
	cout<<"Enter the number a :"<<endl;
	cin>>a;
	cout<<"Enter the number b :"<<endl;
	cin>>b;
	  
	
	cout<<"The value of a == b is:"<<(a==b)<<endl;
	cout<<"The value of a != b is:"<<(a!=b)<<endl;
	cout<<"The value of a >= b is:"<<(a>=b)<<endl;
	cout<<"The value of a <= b is :"<<(a<=b)<<endl;
	cout<<"The value of a > b is :"<<(a>b)<<endl;
	cout<<"The value of a < b is:"<<(a<b)<<endl;
		
}
